  def soft_delete
    @user = User.find(params[:id])
    deleted_email = @user.email + '_deleted_' + I18n.l(Time.current, format: :delete_flag)
    @user.assign_attributes(email: deleted_email, deleted_at: Time.current)
    @user.skip_reconfirmation!
    @user.save!
  end
  
